// 投稿ロジック
// =====================================================

function selectPost() {
  const today = new Date();
  const dayOfYear = Math.floor(
    (today - new Date(today.getFullYear(), 0, 0)) / 86400000
  );
  const hour = today.getHours();
  const timeSlot = hour < 10 ? 0 : hour < 15 ? 1 : hour < 20 ? 2 : 3;

  // 朝: 開発日記（人間味枠）
  if (timeSlot === 0) return DEV_DIARY_POSTS[dayOfYear % DEV_DIARY_POSTS.length];
  // 昼: 問いかけ
  if (timeSlot === 1) return QUESTION_POSTS[dayOfYear % QUESTION_POSTS.length];
  // 夕: 開発ストーリー
  if (timeSlot === 2) return STORY_POSTS[dayOfYear % STORY_POSTS.length];
  // 夜: RPGクラス紹介
  return RPG_CLASS_POSTS[(dayOfYear + 8) % RPG_CLASS_POSTS.length];
}

async function post() {
  const { text } = selectPost();
  console.log("投稿内容:\n", text);

  try {
    const result = await agent.post({ text });
    console.log("✅ 投稿成功:", result.uri);
  } catch (err) {
    console.error("❌ 投稿失敗:", err);
    process.exit(1);
  }
}

post();
